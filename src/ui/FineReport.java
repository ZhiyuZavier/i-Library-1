    private void table_FineReportMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_FineReportMouseClicked
        // TODO add your handling code here:
        try {
			
			int row = table_FineReport.getSelectedRow();
			String table_click = (table_FineReport.getModel().getValueAt(row, 0)
					.toString());
			String sql = "select * from record where recordID=' " + table_click
					+ " ' ";
			pst = db.conn.prepareStatement(sql);
			rs = pst.executeQuery();
			if (rs.next()) {
				String add1 = rs.getString("userID");
				textUserID.setText(add1);
				String add2 = rs.getString("bookID");
				textBookID.setText(add2);
				String add3 = rs.getString("borrowDate");
				textBorrowDate.setText(add3);
				String add4 = rs.getString("returnDate");
				textLostDate.setText(add4);
				String add5 = rs.getString("Fine");
				textFine.setText(add5);
                                String add6 = rs.getString("Paid");
				textPaid.setText(add6);
			}

                        rs.close();
			
		} catch (Exception e) {
			// TODO: handle exception
			JOptionPane.showMessageDialog(null, e);
		}
    }//GEN-LAST:event_table_FineReportMouseClicked

