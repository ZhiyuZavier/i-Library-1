            try {
                String userid = txtUserID.getText();
                String value1 = txtUserName.getText();
                String value2 = txtPhoneNumber.getText();
                String value3 = txtPassword.getText();
                String value4 = txtEmail.getText();

                db.openConn();
                String sql = "update User set userName = '" + value1 + "' ,phoneNumber = '"
                        + value2 + "' ,password = '" + value3
                        + "' ,email = '" + value4 + "' where userID = '"
                        + userid + "' ";
                pst = db.conn.prepareStatement(sql);
                pst.executeUpdate();
                // pst.execute();
                JOptionPane.showMessageDialog(null, "Udpdated successfully");
                db.closeConn();
            } catch (Exception e) {
                // TODO: handle exception
                JOptionPane.showMessageDialog(null, e);
            }

