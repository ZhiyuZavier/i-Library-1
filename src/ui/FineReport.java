    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
         switch(jComboBox1.getSelectedIndex()){
             case 0:
                 try {
           
            String sql = "select * from record where Fine !=''";
            pst = db.conn.prepareStatement(sql);
            rs = pst.executeQuery();
            table_FineReport.setModel(DbUtils.resultSetToTableModel(rs));
            rs.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
            break;
            case 1:
                 try {
           
            String sql = "select * from record where Fine !='' and returnDate !='Lost'";
            pst = db.conn.prepareStatement(sql);
            rs = pst.executeQuery();
            table_FineReport.setModel(DbUtils.resultSetToTableModel(rs));
            rs.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
            break;
            
            case 2:
                try {
           
            String sql = "select * from record where returnDate ='Lost'";
            pst = db.conn.prepareStatement(sql);
            rs = pst.executeQuery();
            table_FineReport.setModel(DbUtils.resultSetToTableModel(rs));
            rs.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
             break;

		default:
		
			break;   
        }
      
    }//GEN-LAST:event_jComboBox1ActionPerformed

